#include <bits/stdc++.h>
using namespace std;

int main() {
  int A, B;
  cin >> A >> B;

  // ここにプログラムを追記
  int c=0;
  cout << "A:";
  while(c<A)
  {
      cout << "]";
      c++;
  }
  
  cout << endl;
  c = 0;
  
  cout << "B:";
  while(c<B)
  {
      cout << "]";
      c++;
  }
  cout << endl;
}
